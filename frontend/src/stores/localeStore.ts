const translations: Record<Language, Record<string, string>> = { zh, en }

// 加载语言设置
const loadLanguage = (): Language => {
  try {
    const saved = localStorage.getItem('app-language')
    if (saved === 'zh' || saved === 'en') {
      return saved
    }
  } catch (e) {
    console.error('Failed to load language:', e)
  }
  // 默认中文
  return 'zh'
}

export const useLocale = create<LocaleState>((set, get) => ({
  language: loadLanguage(),

  setLanguage: (lang) => {
    localStorage.setItem('app-language', lang)
    set({ language: lang })
    // 同步到后端
    api.setLanguage(lang).catch(err => console.error('Failed to set language in backend:', err))
  },

  t: (key: string) => {
    const { language } = get()
    return translations[language][key] || key
  },
}))

// 导出翻译函数供非组件使用
export function t(key: string, lang?: Language): string {
  const language = lang || loadLanguage()
  return translations[language][key] || key
}